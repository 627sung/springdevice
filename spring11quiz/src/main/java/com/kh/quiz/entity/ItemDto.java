package com.kh.quiz.entity;

public class ItemDto {
private int no;
private String name;
private int price;
private String type;
private String when;

public ItemDto() {
	super();
}

public int getNo() {
	return no;
}

public void setNo(int no) {
	this.no = no;
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

public int getPrice() {
	return price;
}

public void setPrice(int price) {
	this.price = price;
}

public String getType() {
	return type;
}

public void setType(String type) {
	this.type = type;
}

public String getWhen() {
	return when;
}

public void setWhen(String when) {
	this.when = when;
}






}
